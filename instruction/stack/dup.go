type Dup struct {
	base.NoOperandsInstruction
}

func (inst *Dup) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	slot := stack.PopSlot()
	stack.PushSlot(slot)
	stack.PushSlot(slot)
}

